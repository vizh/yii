2. ПРОГРАММА КОНФЕРЕНЦИИ RIW 2013

Все три дня работы RIW 2013 в шести залах будут проходить параллельные конференционные мероприятия: секции, круглые столы, мастер-классы и презентации.

Традиционно программа RIW представлена блок-конференциями (набор тематических секций в одном из цветных залов RIW 2013), среди которых в этом году: Реклама, Электронная коммерция, Технологии, Веб-разработка, Кадры, SMM, SEO, Digital Marketing, Мобильные технологии, Государство и право, Краудинвестинг и другие.

Кроме того, в этом году будут работать панели и круглые столы общетематической направленности (Белый зал - White Hall), а также будут проводиться эксклюзивные презентации продуктов, проектов и компаний (Зал презентаций - Presentation Hall).


Неделя Российского Интернета (RIW 2013) пройдет 17-18-19 октября 2013 года в московском Экспоцентре - www.riw13.com

RIW 2013 - это главная ежегодная выставка Рунета, многопотоковая конференция и множество неконференционных мероприятий от компаний-партнеров.

1. ВЫСТАВКА ИНТЕРНЕТ 2013

Выставка проводится в рамках RIW 2013, все 3 дня на территории павильона №3 Экспоцентра на Красной Пресне будут работать стенды компаний-лидеров Рунета, среди которых: Mail.ru Group, RU-CENTER, PayOnline, Ростелеком, 1С-Битрикс, Бегун, LiveTex, СофтКей, Seopult, REG.RU, Subscribe.ru, Деньги Online, USABILITYLAB, Ашманов и партнеры, SAPE, HeadHunter Group, OZON.ru и OZON.travel, Координационный центр доменов RU/РФ, Uniteller, Из рук в руки, RMA, Студия Артемия Лебедева, Каванга! и многие другие.

Всех посетителей выставки ждут консультации специалистов, живое общение с компаниями-лидерами Рунета, промо-продукция и развлекательные мероприятия с приятными бонусами.


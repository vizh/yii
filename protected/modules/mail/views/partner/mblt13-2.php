                                <p><em>15 мая в среду в Москве состоится ежегодная мобильная международная конференция #</em><em>MBLT13.</em></p>
                                <p><a href="http://mblt.ru/"><strong>#MBLT13</strong></a> соберет в одном месте визионеров и экспертов-практиков из Америки, Великобритании, Нидерландов, Италии, Финляндии, Германии, Сингапура и России, чтобы обсудить будущее мобильных технологий в различных сферах бизнеса.</p>
                                <p>В этом году в конференции примут участие более 30 приглашенных докладчиков из ведущих зарубежных и российских компаний: <strong>TechCrunch</strong>, <strong>Foursquare</strong>, <strong>Singularity </strong><strong>University</strong>, <strong>IBM</strong>, <strong>Frog </strong><strong>Design</strong>, <strong>TripAdvisor</strong>, <strong>VentureBeat</strong>, <strong>YOOSE</strong>, <strong>QIWI</strong>,<strong> Сбербанк</strong> и многих других. Специальным гостем станет Алексей Волин — заместитель министра связи и массовых коммуникаций РФ.</p>
                                <p>Программа мероприятия будет поделена по тематическим секциям — финансы, медицина, путешествия, безопасность, реклама, умный город и развлечения. Доклады будут чередоваться с дискуссиями и интервью с лидерами российской и западной мобильной индустрии. Параллельно с основной программой пройдет секция Success Story, где создатели мобильных хитов<strong>LinguaLeo, </strong><strong>Aviasales, </strong><strong>uBank, </strong><strong>Cyto, </strong><strong>Documents</strong> и других расскажут свои историях успеха.</p>
                                <p>На конференции гостей ожидают громкие анонсы, live demo инновационных стартапов и запуск новых продуктов от таких компаний, как <strong>EyeEm, </strong><strong>uBank, </strong><strong>Slon, </strong><strong>Instabank, </strong><strong>Just.</strong><strong>me</strong> и <strong>Angioscan</strong>.</p>
                                <p>Владельцы устройств на базе операционной системы iOS могут бесплатно <a href="https://itunes.apple.com/ru/app/mblt-mezdunarodnaa-mobil-naa/id638113300?mt=8" target="_blank">скачать</a> для своих устройств официальное мобильное приложение #MBLT13.  В нем доступна подробная информация о расписании, докладчиках и всех зарегистрировавшихся участников конференции, а также список ключевых партнеров и адрес проведения мероприятия с контактами организаторов.</p>
                                <p>Подробности и программа мероприятия доступны на сайте конференции <a href="http://mblt.ru/" target="_blank">http://mblt.ru</a>.</p>
                                <p>Приобрести билет на #MBLT13 можно за <strong>11 000 рублей</strong> на <a href="http://mblt2013.runet-id.com/" target="_blank">странице регистрации</a>.</p>
                                <p>Мероприятие пройдет в центре технологического предпринимательства Digital October по адресу: Берсеневская набережная, дом 6, строение 3, 3 этаж.</p>

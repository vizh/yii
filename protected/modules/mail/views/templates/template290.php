<p><strong>UMI.CMS</strong> и <strong>Андрей Терехов </strong>запустили специальный образовательный проект, ориентированный на представителей агентского рынка Рунета &ndash; &laquo;Digital-Сериал&raquo; про вопросы маркетинга и продаж в студиях и агентствах.</p>


<p>Сериал состоит из 5 сезонов, в которые вошли сразу 17 эпизодов (2 текстовые лекции, 9 видео-лекций и 6 эпизодов в режиме ток-шоу с экспертами агентского рынка) &ndash; про различные вопросы продаж, маркетинга, коммуникаций с заказчиком, клиентского сервиса в веб-студии или digital-агентстве.</p>


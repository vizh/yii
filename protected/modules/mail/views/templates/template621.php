<h3>О ФОРУМЕ РИФ.РЕГИОН В СМОЛЕНСКЕ</h3>

<p>Форум РИФ.РЕГИОН.СМОЛЕНСК пройдет в самом центре города, в культурно-выставочном центре имени Тенишевых по адресу ул. Пржевальского, д.3. Он станет большой площадкой для обмена опытом. Помимо гостей из Смоленской, Брянской и Орловской областей, на Форум приглашаются специалисты из близлежащих городов Белоруссии: Витебска, Минска, Могилева и Гомеля.
Соорганизатором и генеральным партнером крупнейшего IT-события в регионе выступит портал Ярмарка Мастеров (www.livemaster.ru). Компания неоднократно становилась победителем премии Рунета, подтверждая звание лучшего интернет-проекта России. В данный момент Ярмарка Мастеров является крупнейшей площадкой для покупки и продажи дизайнерских вещей и авторских handmade-работ в рунете. Ежемесячная аудитория портала насчитывает свыше 5 000 000 уникальных пользователей, а количество представленных товаров перешагнуло порог в 1 500 000.
</p>


<p>Самая важная конференция сезона стартует  <b>6 октября 2015 года в городе Смоленске</b>, в культурно-выставочном центре имени Тенишевых, по адресу ул. Пржевальского, д.3. Компании-лидеры российского интернет-рынка проведут выездной <b>Российский Интернет-Форум</b> (РИФ.Смоленск 2015). В знаковом мероприятии интернет-отрасли примут участие собственники крупнейших IT-компаний страны, ведущие эксперты в сфере интернет-технологий и представители государственного сектора</p>


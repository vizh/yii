<p>ФОМ, Сбербанк, PwC и&nbsp;АСИ, при информационной поддержке RUNET&mdash;ID, запустили проект &laquo;Бизнес в&nbsp;антикризисе&raquo; задачей которого является <strong>формирование сообщества активных предпринимателей, способных противостоять нынешнему кризису</strong>, а&nbsp;также широкое <strong>обсуждение конкретных антикризисных идей</strong>.</p>

<p>В&nbsp;рамках проекта используются исследовательский подход&nbsp;&mdash; опрос бизнесменов, а&nbsp;также социально-коммуникационный подход&nbsp;&mdash; вовлечение активных предпринимателей в&nbsp;совместную работу. <strong>Если вы&nbsp;менеджер или управляющий компании, мы&nbsp;будем благодарны за&nbsp;Ваше участие в&nbsp;проекте:</strong></p>


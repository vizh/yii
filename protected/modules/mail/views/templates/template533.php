<p>Опрос предпринимателей покажет, с&nbsp;какими проблемами сталкивается бизнес в&nbsp;целом, какие решения находит, какие настроения преобладают в&nbsp;бизнес-среде. В&nbsp;рамках социальных коммуникаций будет налажен обмен конкретными эффективными практиками противостояния кризису, историями успеха, будут сформулированы главные задачи, которые сегодня необходимо решать государству.</p>

